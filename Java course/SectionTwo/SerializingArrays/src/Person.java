import java.io.Serializable;

// a java Person class
public class Person implements Serializable {
    private static final long serialVersionUID = 1l;
    private int id;
    private String name;

    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
